;GAME.ASM

PREVIOUS_INPUT	EQU	20H.0
INPUT	EQU	P0.0
JUMPING	EQU	21H
JUMP_DURATION	EQU	22H

	JMP	INIT

INIT:
	CLR	PREVIOUS_INPUT
	CLR	INPUT
	MOV	JUMP_DURATION, #5
	JMP	START

CHECK_INPUT:
	MOV	A, INPUT
	MOV	B, PREVIOUS_INPUT
	XRL	A, B
	JNZ	INPUT_CHANGED
	JMP	INPUT_NOT_CHANGE

INPUT_CHANGED:
INPUT_NOT_CHANGED:

START:
	MOV A, JUMPING
	JNZ NOT_JUMPING
	CALL	CHECK_INPUT

NOT_JUMPING: